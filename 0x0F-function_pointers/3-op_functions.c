#include "3-calc.h"

/**
* op_mod - modulo
* @a: integer
* @b: integer
* Return: int
*/
int op_mod(int a, int b)
{
	return (a % b);
}

/**
* op_div - division
* @a: integer
* @b: integer
* Return: int
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mul - multiplication
* @a: integer
* @b: integer
* Return: int
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_sub - substruction
* @a: integer
* @b: integer
* Return: int
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_add - addition
* @a: integer
* @b: integer
* Return: int
*/
int op_add(int a, int b)
{
	return (a + b);
}
